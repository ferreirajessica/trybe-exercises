/* Faça um programa que, dado um valor recebido como parâmetro, retorne "positive" 
se esse valor for positivo, "negative" se for negativo, e caso não seja nem positivo 
e nem negativo retorne "zero". */

const valueOne = 0;
const valueTwo = 15;
const valueThree = -7;

if (valueOne > 0) {
    console.log('Positive');
} else if (valueOne < 0) {
    console.log('Negative');
} else {
    console.log('Zero');
};

if (valueTwo > 0) {
    console.log('Positive');
} else if (valueTwo < 0) {
    console.log('Negative');
} else {
    console.log('Zero');
};

if (valueThree > 0) {
    console.log('Positive');
} else if (valueThree < 0) {
    console.log('Negative');
} else {
    console.log('Zero');
};